import { Metadata } from "next";
import Footer from "./Footer";
import Header from "./Header";
import "./output.css";
import Providers from "../components/providers";
import Modal from "@/components/actions/modal/Modal";
import { Analytics } from "@vercel/analytics/react";
import LayoutWOSidebar from "@/components/layouts/LayoutWOSidebar";
import { getProjects } from "@/lib/prisma";
export const metadata: Metadata = {
  title: "Etan Heyman`n Portfolio",
  description: "Generated by create next app",
};

export default async function RootLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  const { projects, error } = await getProjects();
  if (error || !projects) {
    return <div>No projects were found, {error}</div>;
  }
  return (
    <html lang="en">
      <body className="scrollbar-none h-full max-h-screen overflow-hidden bg-black">
        <Providers>
          {/* <MouseOverlay> */}
          <Header projects={projects} />
          <LayoutWOSidebar> {children}</LayoutWOSidebar>
          <Modal />
          <Footer />
          {/* </MouseOverlay> */}
        </Providers>
        <Analytics />
      </body>
    </html>
  );
}
